    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tblHienThi.getSelectedRowCount();
        dtm = (DefaultTableModel) tblHienThi.getModel();
        if (row >= 0) {
            dtm.setValueAt(txtMa.getText(), row, 0);
            dtm.setValueAt(txtTen.getText(), row, 1);
            dtm.setValueAt(String.valueOf(txtTuoi.getText()), row, 2);
            dtm.setValueAt(cboBac.getSelectedItem(), row, 3);
            dtm.setValueAt(cboLoai.getSelectedItem(), row, 4);
            if (rdoNam.isSelected()) {
                tblHienThi.setValueAt("Nam", row, 5);
            } else {
                tblHienThi.setValueAt("Nữ", row, 5);
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

