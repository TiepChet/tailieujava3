    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Trong ma");
        } else if (!txtMa.getText().matches("[a-z A-Z]+")) {
            JOptionPane.showMessageDialog(rootPane, "Ma dang chu");
        } else if (txtTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Trong ten");
        } else if (!txtTen.getText().matches("[a-z A-Z]+")) {
            JOptionPane.showMessageDialog(rootPane, "Ten dang chu");
        } else if (txtTuoi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Trong tuoi");
        } else {
            String code = txtMa.getText();
            String name = txtTen.getText();
            int age = Integer.valueOf(txtTuoi.getText());
            String step = (String) cboBac.getSelectedItem();
            String type = (String) cboLoai.getSelectedItem();
            boolean gender = rdoNam.isSelected();

            GiangVien giangVien = new GiangVien(code, name, age, step, type, gender);
            listGiangVien.add(giangVien);
            showDataTable(listGiangVien);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void fillData(int index) {
        GiangVien giangVien = listGiangVien.get(index);
        txtMa.setText(giangVien.getMaGiangVien());
        txtTen.setText(giangVien.getTenGiangVien());
        txtTuoi.setText(String.valueOf(giangVien.getTuoi()));
        cboBac.setSelectedItem(giangVien.getBac());
        cboLoai.setSelectedItem(giangVien.getLoai());
        boolean gender = giangVien.isGioiTinh();
        if (gender == true) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    private void showDataTable(List<GiangVien> list) {
        dtm.setRowCount(0);
        for (GiangVien giangVien : list) {
            dtm.addRow(giangVien.toDataRow());
        }
    }

